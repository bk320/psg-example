print("🧼 Guía rápida: Cómo lavar los platos como un pro 😎")

print("\n1. Lo que vas a necesitar:")
print("- Una esponja (de esas que tienen un lado suave y otro rugoso).")
print("- Jabón para platos.")
print("- Guantes si no quieres mojarte las manos.")
print("- Un trapito limpio o un escurridor para secarlos.")
print("- Agua calentita, ideal para quitar la grasa.")

print("\n2. Antes de empezar...")
print("- Quita los restos de comida de los platos.")
print("- Puedes usar una servilleta o cuchara para lo más pegado.")
print("- Dales una enjuagadita rápida si están muy cochinos.")

print("\n3. ¿En qué orden los lavo?")
print("👉 Primero los vasos (para que no queden grasosos).")
print("👉 Luego los cubiertos.")
print("👉 Después los platos chicos.")
print("👉 Sigue con los platos grandes.")
print("👉 Y al final las ollas y sartenes (porque están más cochinas).")

print("\n4. ¡A lavar!")
print("- Pon un poco de jabón en la esponja.")
print("- Frota bien por todos lados, sobre todo donde hay grasa.")
print("- No te olvides de los bordes y el fondo.")
print("- Enjuágalos bien con agua, que no queden con jabón.")

print("\n5. ¿Y el secado?")
print("- Puedes dejarlos en el escurridor o secarlos con un trapito.")
print("- Asegúrate de que estén bien secos antes de guardarlos.")

print("\n6. ¡No olvides limpiar tu espacio!")
print("- Enjuaga la esponja y déjala escurrir.")
print("- Limpia el lavadero.")
print("- Guarda todo y listo.")

print("\n🎉 ¡Y ya está! Tus platos están limpios y tú te mereces un descanso 🍵")
